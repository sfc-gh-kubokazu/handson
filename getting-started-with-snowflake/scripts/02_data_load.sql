-- =============================================================
-- STEP 3: データのロード
-- 対応手順書: ../steps/03_data_load.md
-- =============================================================
-- このファイルでは以下を実施します:
--   1. ウェアハウスのサイズ変更（ロード高速化）
--   2. COPY INTO コマンドで S3 → Snowflake にデータをロード
--   3. アナリティクス用ウェアハウスの作成
-- =============================================================

USE ROLE sysadmin;
USE WAREHOUSE compute_wh;
USE DATABASE citibike;
USE SCHEMA public;

-- =============================================================
-- 1. データロード用にウェアハウスをサイズアップ
-- =============================================================

-- 現在のウェアハウスサイズを確認
SHOW WAREHOUSES;

-- ⚠️ Small (デフォルト) → Large に変更（4xのコンピュートパワー）
-- ロード完了後は自動的にサスペンドされるので余分なクレジットは消費しません
ALTER WAREHOUSE compute_wh SET WAREHOUSE_SIZE = 'large';

-- =============================================================
-- 2. データのロード（COPY INTO）
-- =============================================================

-- ステージからテーブルにデータをコピー
-- ⚠️ 注意: 初回ロードには数分かかります（正常です）
-- 「COPY executed with 0 files processed.」と表示される場合、
--   すでにロード済みのファイルはスキップされます（Snowflakeの冪等性）
COPY INTO trips
FROM @citibike_trips
FILE_FORMAT = csv
PATTERN = '.*csv.*';

-- ロードしたデータを確認（先頭20件）
SELECT * FROM trips LIMIT 20;

-- 件数確認
SELECT COUNT(*) AS total_trips FROM trips;

-- =============================================================
-- 3. ウェアハウスをデフォルトサイズに戻す
-- =============================================================

-- データロード完了後、ウェアハウスサイズを元に戻す
-- ⚠️ コスト管理のため、大規模ロードが終わったら必ずサイズダウンする
ALTER WAREHOUSE compute_wh SET WAREHOUSE_SIZE = 'small';

-- =============================================================
-- 4. アナリティクス専用ウェアハウスの作成
-- =============================================================

-- データロード用とクエリ用でウェアハウスを分けることがベストプラクティス
-- 理由: リソース競合を防ぎ、それぞれのワークロードに最適なサイズを設定できる
CREATE OR REPLACE WAREHOUSE analytics_wh
    WITH WAREHOUSE_SIZE = 'large'
    AUTO_SUSPEND = 300       -- 5分間アイドル状態が続いたら自動停止
    AUTO_RESUME = TRUE       -- クエリが来たら自動起動
    COMMENT = 'アナリティクスクエリ専用ウェアハウス';

-- 以降のクエリはアナリティクス用ウェアハウスを使用
USE WAREHOUSE analytics_wh;

-- 動作確認
SELECT COUNT(*) AS total_trips FROM trips;
